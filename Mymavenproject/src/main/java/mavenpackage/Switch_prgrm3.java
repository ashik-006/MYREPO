package mavenpackage;

public class Switch_prgrm3 {

	public static void main(String[] args)
	{
		String weeks="wed";
		switch (weeks)
		{
		case "sun":System.out.println("sun is a day");
		break;
		case "mon":System.out.println("mon is a day");
		break;
		case "tue":System.out.println("tue is a day");
		break;
		case "wed":System.out.println("wed is a day");
		break;
		case "thurs":System.out.println("thurs is a day");
		break;
		case "fri":System.out.println("fri is a day");
		break;
		case "sat":System.out.println("sat is a day");
		break;
		default:System.out.println("invalid");
		
		}

	}

}
